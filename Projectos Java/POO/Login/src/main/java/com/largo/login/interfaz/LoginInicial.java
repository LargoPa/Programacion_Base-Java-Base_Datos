    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        String usuario = txtUsuario.getText();
        String contrasenia = txtContrasenia.getText();
        Usuario usr = control.validarUsuario(usuario,contrasenia);
        
        if(usr!=null){
            String rol = usr.getUnRol().getNombreRol();
            
            if(rol.equals("admin")){
                PrincipalAdmin pantallaAdmin = new PrincipalAdmin(control,usr);
                pantallaAdmin.setVisible(true);
                pantallaAdmin.setLocationRelativeTo(null);
                this.dispose();
            }
            if(rol.equals("user")){
                PrincipalUser pantallaUser = new PrincipalUser(control,usr);
                pantallaUser.setVisible(true);
                pantallaUser.setLocationRelativeTo(null);
                this.dispose();
            }
        }
        else{
            txtMensaje.setText("Usuario o contraseña incorrectos.");
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

    

